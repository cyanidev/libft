#include<stdio.h>
int	ft_isalpha(int c)
{
	if ((c >= 65 && c <= 90) || (c >= 97 && c <= 122))
		return (1);
	else
		return (0);
}

int	main(void)
{
	int	p;

	p = 164;
	printf ("%d", ft_isalpha(p));
}
