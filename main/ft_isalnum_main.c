#include<stdio.h>

int ft_isalnum(int c)
{
	if ((c >= 48 && c <= 57) || (c >= 97 && c <= 122) || (c >= 65 && c <= 90))
		return (1);
	else
		return (0);
}

int	main(void)
{
	int	p;

	p = 164;
	printf ("%d", ft_isalnum(p));
}
